def map_hex_color_to_xterm_color(hex_value: str):
    """map_color expects a hex_value as input
        can, or not, have hash infront. """

    # Convert HEX to LabColor object
    # First, turn HEX to sRGBColor object
    rgb = sRGBColor.new_from_rgb_hex(hex_value)

    # Second, convert rgb to lab
    labcolor = convert_color(rgb, LabColor)

    # Find the color from lab_object that is most similar to labcolor
    lowest_d_val = sys.maxsize
    position_d_val = sys.maxsize

    position = 0
    for obj in lab_objects:
        delta_e = delta_e_cie2000(labcolor, obj)
        if delta_e < lowest_d_val:
            lowest_d_val = delta_e 
            position_d_val = position
            
        position = position + 1

    return lowest_d_val, position_d_val
